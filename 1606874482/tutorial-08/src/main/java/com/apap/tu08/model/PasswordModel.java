package com.apap.tu08.model;

public class PasswordModel {
	private String passwordLama;
	private String passwordBaru;
	private String passwordBaruKonfirmasi;
	public String getPasswordLama() {
		return passwordLama;
	}
	public void setPasswordLama(String passwordLama) {
		this.passwordLama = passwordLama;
	}
	public String getPasswordBaru() {
		return passwordBaru;
	}
	public void setPasswordBaru(String passwordBaru) {
		this.passwordBaru = passwordBaru;
	}
	public String getPasswordBaruKonfirmasi() {
		return passwordBaruKonfirmasi;
	}
	public void setPasswordBaruKonfirmasi(String passwordBaruKonfirmasi) {
		this.passwordBaruKonfirmasi = passwordBaruKonfirmasi;
	}
	
}